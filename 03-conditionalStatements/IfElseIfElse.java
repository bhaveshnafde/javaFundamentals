public class IfElseIfElse{
  public static void main(String args[]){
    int num1 = 30,num2 = 30;
    if(num1>num2){
      System.out.println("Num 1 is greater");
    }else if(num1<num2){
      System.out.println("Num 2 is greater.");
    }else{
      System.out.println("Num 1 is equal to num 2.");
    }
  }
}

