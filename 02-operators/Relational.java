public class Relational{
  public static void main(String args[]){
    int a = 10,b=2;
    System.out.println("a==b    : "+(a==b));
    System.out.println("a>=b    : "+(a>=b));
    System.out.println("a<=b    : "+(a<=b));
    System.out.println("a!=b    : "+(a!=b));
    System.out.println("a>b     : "+(a>b));
    System.out.println("a<b     : "+(a<b));
  }
}
/*
a==b    : false
a>=b    : true
a<=b    : false
a!=b    : true
a>b     : true
a<b     : false
*/
