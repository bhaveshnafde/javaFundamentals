public class Bitwise{
  public static void main(String args[]){

    int a = 5; //0101
    int b = 7; //0111

    System.out.println("a&b   : "+(a&b));
    System.out.println("a^b   : "+(a^b));
    System.out.println("a|b   : "+(a|b));
    System.out.println("~a    : "+(~a));
  }
}

/*
a&b   : 5
a^b   : 2
a|b   : 7
~a    : -6
*/
